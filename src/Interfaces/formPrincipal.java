    private void btnGuardarComidaActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnGuardarComidaActionPerformed
        if(txtNombreComida.getText().isEmpty()||txtDetalleComida.getText().isEmpty()||txtCantCalorias.getText().isEmpty()||comboComida.getSelectedItem()==null){
            JOptionPane.showMessageDialog(this,"Debe completar todos los campos");
        }else{
            ComidaData com = new ComidaData();
            com.guardarComida(txtNombreComida,txtDetalleComida,txtCantCalorias,comboComida);
            txtNombreComida.setText("");
           txtDetalleComida.setText("");
           txtCantCalorias.setText("");
           comboComida.setSelectedItem(null);
           txtNombreComida.requestFocus();
        }
    }//GEN-LAST:event_btnGuardarComidaActionPerformed

